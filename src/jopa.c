void paint(double values[WIDTH]) {
    for (int i = 0; i < HEIGHT; i++) {
        // printf("%d ", i);
        for (int j = 0; j < WIDTH; j++) {
            if (values[j] == i)
                printf("*");
            else
                printf(".");
        }
        printf("\n");
    }
}

void invalues(double values[WIDTH]) {
    for (int i = 0; i < WIDTH; i++) {
        values[i] = values[i] + 12;
    }
}

void rounding(double values[WIDTH]) {
    for (int i = 0; i < WIDTH; i++) {
        values[i] = round(values[i] * 12);
        if (values[i] > 12) values[i] = 12;
        if (values[i] < -12) values[i] = -12;
        printf("%f\n", values[i]);
    }
}

